//Sequencias de caractés de escape

// \n adiciona uma nova linha
Console.WriteLine("Pedro\nMaia");
// \t adiciona um espaçamento
Console.WriteLine("Hello\tWorld!");
// \" adiciona aspas duplas
Console.WriteLine("Vamos dar um \"jeitinho\" para conseguir.");
// Para inserir uma barra invertida você duplica a \\
Console.WriteLine("Vamos dar um \"jeitinho\" para conseguir");

//Caracteres Unicode
Console.WriteLine("\u3053\u3093\u306B\u3061\u306F World!");
